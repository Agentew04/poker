namespace InstaPoker.Client.Graphics.Styles;

/// <summary>
/// Describes the vertical alignment of an object.
/// </summary>
public enum VerticalAlign {
    /// <summary>
    /// Aligns an object to the top of the bounding box.
    /// </summary>
    Top,
    /// <summary>
    /// Centers an object in the vertical axis. 
    /// </summary>
    Center,
    /// <summary>
    /// Align an object to the bottom of the vertical axis.
    /// </summary>
    Bottom
}

/// <summary>
/// Describes the horizontal alignment of an object.
/// </summary>
public enum HorizontalAlign {
    /// <summary>
    /// Aligns an object to the left side of the bounding box.
    /// </summary>
    Left,
    /// <summary>
    /// Centers the element horizontally.
    /// </summary>
    Center,
    /// <summary>
    /// Aligns the object to the right size of the bounding box.
    /// </summary>
    Right
}
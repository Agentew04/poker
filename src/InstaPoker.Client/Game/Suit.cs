namespace InstaPoker.Client.Game;

/// <summary>
/// Represents a card suit. 
/// </summary>
public enum Suit {
    /// <summary>
    /// Suit represented by a red losangole.
    /// </summary>
    Diamonds,
    /// <summary>
    /// Suit represented by a black little tree-leafed clover.
    /// </summary>
    Clubs,
    /// <summary>
    /// Suit represented by a red heart.
    /// </summary>
    Hearts,
    /// <summary>
    /// Suit represented by a black upside down heart, with a little knob below.
    /// </summary>
    Spades,
}